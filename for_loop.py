#loops in python
#for loop
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
    print(fruit)

#for loop using range function
for i in range(5):
    print(i)

#range(start, stop, step)
for i in range(1, 10, 2):
    print(i)

#in the string
word = "Python"
for letter in word:
    print(letter)
