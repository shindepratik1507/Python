#operator in python
#Assignment operator

x = 10

x += 5   # x = x + 5
print(x) # Output: 15

x -= 3   # x = x - 3
print(x) # Output: 12

x *= 2   # x = x * 2
print(x) # Output: 24

x /= 4   # x = x / 4
print(x) # Output: 6.0

x %= 5   # x = x % 5
print(x) # Output: 1.0

x **= 3  # x = x ** 3
print(x) # Output: 1.0

x //= 2  # x = x // 2
print(x) # Output: 0.0
